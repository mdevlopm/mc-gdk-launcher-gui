_current_lang = "tr"

def init_i18n():
    global _current_lang
    cfg = load_cfg()
    lang = cfg.get("language")
    if not lang:
        lang = "en"
        try:
            import locale
            import os
            sys_lang = locale.getdefaultlocale()[0] or os.environ.get("LANG", "")
            if sys_lang:
                sys_lang = sys_lang.lower()
                if sys_lang.startswith("tr"):
                    lang = "tr"
                elif sys_lang.startswith("de"):
                    lang = "de"
        except Exception:
            pass
    _current_lang = lang

def get_current_lang():
    return _current_lang

def set_current_lang(code):
    global _current_lang
    if code in _STRINGS:
        _current_lang = code

def _t(key, **kwargs):
    """
    Belirtilen anahtar için çeviriyi döner.
    Eğer anahtar belirtilen dilde yoksa Türkçe'ye, o da yoksa anahtarın kendisine döner.
    """
    lang = _current_lang
    text = _STRINGS.get(lang, {}).get(key)
    if text is None:
        text = _STRINGS.get("tr", {}).get(key, key)
    
    if kwargs:
        return text.format(**kwargs)
    return text

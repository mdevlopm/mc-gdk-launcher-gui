_current_lang = "tr"

def init_i18n():
    global _current_lang
    cfg = load_cfg()
    lang = cfg.get("language")
    if not lang:
        lang = "en"
        sys_lang = ""
        try:
            import os
            sys_lang = os.environ.get("LANG", "")
        except Exception:
            pass
        if not sys_lang:
            try:
                import locale
                sys_lang = (locale.getlocale()[0] or locale.getdefaultlocale()[0] or "")
            except Exception:
                pass
        
        if sys_lang:
            sys_lang = sys_lang.lower()
            if sys_lang.startswith("tr"):
                lang = "tr"
            elif sys_lang.startswith("de"):
                lang = "de"
            elif sys_lang.startswith("zh"):
                lang = "zh"
    _current_lang = lang

def get_current_lang():
    return _current_lang

def set_current_lang(code):
    global _current_lang
    if code in _STRINGS:
        _current_lang = code

def _t(key, **kwargs):
    """
    Belirtilen anahtar için çeviriyi döner.
    Gelişmiş Fallback Sistemi: Seçilen dil -> İngilizce -> Türkçe -> Anahtar
    """
    lang = _current_lang
    text = _STRINGS.get(lang, {}).get(key)
    if text is None:
        text = _STRINGS.get("en", {}).get(key)
    if text is None:
        text = _STRINGS.get("tr", {}).get(key, key)
    
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text
